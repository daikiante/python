# functions of list

list = [1,2,3,4,5,6,7,7,7]

# sum
print(sum(list))

# count :calculates total repeat of given element of list
print(list.count(7))

# len :calculates total length of list
print(len(list))

# index : location
print(list.index(6))

# min & max : check
print(min(list))

print(max(list))